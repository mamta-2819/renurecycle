<div class="environment-aboutus-text">

<p>You can extract glass from CRTs (Cathode Ray Tubes) of computer monitors and televisions. But there’s a little problem here. CRTS contains several hazardous substances, such as lead. And this is dangerous to both human health and the immediate environment. This makes it difficult to retrieve a glass from CRTs.

However, there are certain steps you may take to ensure safer CRT recycling.

First, separate the CRT from the monitor or television. Then shred the CRT into small pieces. Remove the metals with over-band magnets. This helps you remove ferrous and even non-ferrous objects from that glass.

After this, use washing lines to clear phosphors and oxides from that glass. The last step is called glass sorting. This is where you separate non-leaded from leaded gas. You can then use the extract to make newer screens.</p>
